using UnityEngine;

public class AudioManager : MonoBehaviour

{
    private static AudioManager instance;

    void Awake()
    {
        if (instance == null)
        {
            instance = this;
            transform.SetParent(null);
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
